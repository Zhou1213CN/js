export default function MaterialIcon(type){
    switch(type){
        case 'Code':
            return '<i class="fab fa-python"></i>';
        case 'Report':
            return '<i class="far fa-file-alt"></i>';
        case 'PPT':
            return '<i class="far fa-file-powerpoint"></i>';
        case 'Paper':
            return '<i class="far fa-file-alt"></i>';
        case 'Slides':
            return '<i class="far fa-file-powerpoint"></i>';
    }
}
